
public class ForLoopDemo
{
	
	public static void main (String[] args)
	{
		for(int i = 0; i < 11; i++)
		{	System.out.print(i + " ");
		}
		System.out.println();
		System.out.println("---------------");
		for(int i = 10; i >= 0; i--)
		{	System.out.print(i + " ");
		}
		System.out.println();
		System.out.println("---------------");
		for(int i = 0; i < 21; i=i+2)
		{	System.out.print(i + " ");
		}
	}
}

